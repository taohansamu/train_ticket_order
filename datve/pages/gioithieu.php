<p>Tính đến năm 1971 - 1972 thì Việt Nam Cộng hòa có 1.240 km đường sắt nhưng vì chiến cuộc và an ninh nên chỉ có 57% sử dụng được. Dù vậy, tổng lượng hành khách và hàng hóa chuyên chở bằng đường sắt tăng dần. </p>
</ul>

