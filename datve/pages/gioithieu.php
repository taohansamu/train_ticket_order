<li><b>Thời kỳ từ 1945 đến 1954</b></li>
<p>Ngay sau khi Cách mạng Tháng Tám thành công, ngày 28 tháng 8 năm 1945, Chủ tịch Hồ Chí Minh đã Chính thức ký Quyết định thành lập Bộ Giao thông Công chính thuộc Chính phủ nước Việt Nam Dân chủ Cộng hoà, trong đó có Sở Hỏa Xa - Cơ quan Quản lý Nhà nước và khai thác về Đường sắt Việt Nam. Và từ đây, Hệ thống Đường sắt Việt Nam đã thực sự trở thành tài sản của người Việt Nam, phục vụ người dân nước Việt.</p>
<li><b>Thời kỳ từ 1954 đến 1975</b></li>
<ul class="w3-ul">
<li>Cơ cấu tại miền Bắc</li>
<p>Đường sắt xây dựng CNXH ở miền Bắc và chi viện cho miền Nam. Thời kỳ này, do nhiệm vụ mới của Bắc Việt là khôi phục phát triển kinh tế miền Bắc để chi viện cho chiến trường miền Nam, Chính phủ Việt Nam Dân chủ Cộng hòa đã quyết định Thành lập Tổng cục Đường sắt trực thuộc Bộ Giao thông và Bưu điện (trước đó là Bộ Giao thông Công chính) do Ông Nguyễn Văn Trân Bộ trưởng Bộ Giao thông và Bưu điện kiêm Tổng cục trưởng; các Tổng cục trưởng tiếp theo là Hồng Xích Tâm Thứ trưởng kiêm nhiệm, Hà Đăng Ấn.</p>
<p>Trong 10 năm (1954 – 1964), Hệ thống Đường sắt miền Bắc đã được xây dựng và khôi phục lại với những tuyến đường chính là Hà Nội – Lào Cai; Hà Nội - Hải Phòng, Hà Nội - Lạng Sơn. Những tuyến đường này đã có vai trò quan trọng phục vụ đi lại, khôi phục kinh tế miền Bắc trong nhiều năm thời điểm đó và còn phát huy tác dụng đến bây giờ. Một Tuyến Đường sắt khác được xây dựng là Hà Nội – Thái Nguyên với 164 km cũng hoàn thành trong giai đọan này.</p>
<p>Vận tải Đường sắt trong 10 năm (1954 – 1964) đã đảm nhận trên 20% khối lượng vận chuyển toàn ngành GTVT, thực hiện sản lượng luân chuyển hàng hoá trên 50%.</p>
<p>Công nghiệp Đường sắt được hình thành với một số chuyên ngành tuy còn hạn hẹp nhưng đã tự túc sản xuất được một số mặt hàng phục vụ ngành, nổi bật nhất là Nhà máy Xe lửa Gia Lâm đã hình thành được nhiều phân xưởng quan trọng làm tiền đề cho công nghiệp đóng tàu Đường sắt sau này như phân xưởng sửa chữa đầu máy, đóng mới toa xe, rèn, đúc v.v...<p>
<p>Đường sắt miền Bắc chống chiến tranh phá hoại miền Bắc của Hoa Kỳ và chi viện cho giải phóng miền Nam. Đây là thời kỳ đầu đánh dấu bước trưởng thành vượt bậc của Ngành Đường sắt kể từ khi thành lập. Hai đặc điểm nổi bật của Ngành thời kỳ này là phục vụ sự nghiệp củng cố và phát triển kinh tế miền Bắc và chi viện cho cuộc kháng chiến chống Mỹ ở chiến trường miền Nam. Cũng trong giai đoạn này, Ngành Đường sắt đã nhận được một sự đầu tư đáng kể của Nhà nước và viện trợ giúp đỡ của bạn bè quốc tế, đặc biệt là sự giúp đỡ của Liên Xô và Trung Quốc.</p>
<p>Ngành Đường sắt, liên tục đảm bảo giao thông suốt trong điều kiện bị Mỹ đánh phá dữ dội. Ngành Đường sắt đã làm 3.915 mét cầu tạm, 82 kilô mét đường và 274,5 kilô mét dây thông tin và vận chuyển được 4,16 triệu tấn hàng hoá. Cán bộ, nhân viên và tự vệ Ngành Đường sắt đã bắn rơi hàng chục máy bay các loại và dò phá được hàng ngàn quả bom nổ chậm ở các chiến trường trọng điểm miền Nam.</p>
<li>Cơ cấu tại miền Nam</li>
